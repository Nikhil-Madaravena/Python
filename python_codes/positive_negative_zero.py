'''66 Develop a program that reads integer number and prints whether it's positive
, negative, or zero using if-elif-else statements'''
num= int(input("enter a number:"))
if num>0:
    print(f"{num} is a positive number")
elif num<0:
    print(f"{num} is a negative number")
else:
    print(f"{num} is nighter a positive nor a negitive it is zero")
    
