def add(a,b):
    print(f"{a} + {b} = {a+b}")
def sub(a,b):
    print(f"{a} - {b} = {a-b}")
def product(a,b):
    print(f"{a} * {b} = {a*b}")
def div(a,b):
    print(f"{a} / {b} = {a/b}")
def f_div(a,b):
    print(f" {a} // {b} = {a//b}")
def m_div(a,b):
    print(f"{a} % {b} = {a%b}")
def powe(a,b):
    print(f"{a} ^ {b} = {a**b}")