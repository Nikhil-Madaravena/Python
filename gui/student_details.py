e4m1=Entry(m1, width=30, textvariable=vattendence)
l5m1=Label(m1,text="enter your CGPA:-")
e5m1=Entry(m1, width=30, textvariable=vcgpa)
l6m1=Label(m1,text="enter your mobile number:-")
e6m1=Entry(m1, width=30, textvariable=vnumber)
l7m1=Label(m1,text="enter your address:-")
e7m1=Entry(m1, width=60, textvariable=vaddress)
b=Button(m1,text="submit",command=display)

l1m1.grid(row=0,column=0,pady=2)
