from tkinter import *
m1 = Tk()
m1.geometry("500x500")
m1.title("Student Information Entry")
m2=Tk()
m2.geometry("500x500")
m2.title("Student Information Display")

vname=StringVar()
vyear=IntVar()
vbranch=StringVar()
vattendence=StringVar()
vcgpa=StringVar()
vnumber=IntVar()
vaddress=StringVar()

def display():
    name=vname.get()
    year=vyear.get()
    branch=vbranch.get()
    attendence=vattendence.get()
    cgpa=vcgpa.get()
    number=vnumber.get()
    address=vaddress.get()

    l1cm2.config(text=name)
    l2cm2.config(text=year)
    l3cm2.config(text=branch)
    l4cm2.config(text=attendence)
    l5cm2.config(text=cgpa)
    l6cm2.config(text=number)
    l7cm2.config(text=address)

    vname.set("")
    vyear.set("")
    vbranch.set("")
    vattendence.set("")
    vcgpa.set("")
    vnumber.set("")
    vaddress.set("")

    m1.destroy()

l1m1 = Label(m1, text=" student name:-")
e1m1=Entry(m1, width=30, textvariable=vname)
